    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String mode = jComboBox1.getSelectedItem().toString();
        Tweet tweets = new Tweet();
        switch (mode) {
            case "Search Tweets":
                {
                    jTextArea4.removeAll();
                    
                    tweets.newGetTweets(jTextField2.getText(), jTextArea1,jTextArea4);
                    break;
                }
                case "Search From Users":
                {
                    jTextArea4.removeAll();
                    
                    tweets.getUserTweets(jTextField2.getText(),jTextArea1,jTextArea4);
                    break;
                }
                case "Current Trends Tweets":
                {
                    jTextArea4.removeAll();
                    
                    tweets.getCurrentTrends(1, jTextArea1,jTextArea4);
                    break;
                }
        }
    }//GEN-LAST:event_jButton1MouseClicked

