    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String mode = jComboBox1.getSelectedItem().toString();
        switch (mode) {
            case "Search Tweets":
                {
                    jTextArea1.removeAll();
                    jTextArea2.setText("Searching tweets using hashtags. \n( Input words no need #.) ");
                    break;
                }
                case "Search From Users":
                {
                    jTextArea1.removeAll();
                    jTextArea2.setText("Searching tweets using username. \n( Input words no need @.) ");
                    break;
                }
                case "Current Trends Tweets":
                {
                    jTextField2.setText("Worldwide");
                    jTextArea2.setText("Get current world 50 trends. \n(No need to input keywords.) ");
                    break;
                }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

